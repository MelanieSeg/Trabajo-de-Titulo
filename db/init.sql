CREATE TABLE IF NOT EXISTS custom_metric_values (
    id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    custom_metric_id INTEGER NOT NULL REFERENCES custom_metrics(id) ON DELETE CASCADE,
    facility_id INTEGER REFERENCES facilities(id) ON DELETE SET NULL,
    year INTEGER NOT NULL,
    month INTEGER NOT NULL,
    value DOUBLE PRECISION NOT NULL,
    created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    CONSTRAINT uq_custom_metric_value_month UNIQUE (custom_metric_id, facility_id, year, month),
    CONSTRAINT ck_custom_metric_values_month CHECK (month >= 1 AND month <= 12),
    CONSTRAINT ck_custom_metric_values_year CHECK (year >= 2000 AND year <= 2100)
