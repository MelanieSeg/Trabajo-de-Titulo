-- EcoEnergy PostgreSQL schema
-- Idempotent initialization script.
-- Safe to execute multiple times.

BEGIN;

CREATE EXTENSION IF NOT EXISTS pgcrypto;
CREATE EXTENSION IF NOT EXISTS citext;

CREATE OR REPLACE FUNCTION set_updated_at_timestamp()
RETURNS TRIGGER AS $$
BEGIN
    NEW.updated_at = NOW();
    RETURN NEW;
END;
$$ LANGUAGE plpgsql;

-- =========================================================
-- Core organization structure
-- =========================================================

CREATE TABLE IF NOT EXISTS companies (
    id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name VARCHAR(150) NOT NULL UNIQUE,
    industry VARCHAR(120),
    legal_name VARCHAR(180),
    country_code VARCHAR(2),
    timezone VARCHAR(64) NOT NULL DEFAULT 'America/Santiago',
    created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    updated_at TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

CREATE TABLE IF NOT EXISTS facilities (
    id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    company_id INTEGER NOT NULL REFERENCES companies(id) ON DELETE CASCADE,
    code VARCHAR(40),
    name VARCHAR(150) NOT NULL,
    region VARCHAR(120),
    city VARCHAR(120),
    address TEXT,
    latitude DOUBLE PRECISION,
    longitude DOUBLE PRECISION,
    status VARCHAR(20) NOT NULL DEFAULT 'active',
    created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    updated_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    CONSTRAINT uq_facility_company_name UNIQUE (company_id, name),
    CONSTRAINT uq_facility_company_code UNIQUE (company_id, code),
    CONSTRAINT ck_facility_status CHECK (status IN ('active', 'inactive', 'maintenance'))
);

CREATE TABLE IF NOT EXISTS utility_types (
    id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    code VARCHAR(30) NOT NULL UNIQUE,
    name VARCHAR(80) NOT NULL,
    default_unit VARCHAR(20) NOT NULL,
    description TEXT
);

CREATE TABLE IF NOT EXISTS facility_areas (
    id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    facility_id INTEGER NOT NULL REFERENCES facilities(id) ON DELETE CASCADE,
    name VARCHAR(80) NOT NULL,
    description TEXT,
    created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    CONSTRAINT uq_facility_area_name UNIQUE (facility_id, name)
);

CREATE TABLE IF NOT EXISTS meters (
    id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    facility_id INTEGER NOT NULL REFERENCES facilities(id) ON DELETE CASCADE,
    utility_type_id INTEGER NOT NULL REFERENCES utility_types(id) ON DELETE RESTRICT,
    area_id INTEGER REFERENCES facility_areas(id) ON DELETE SET NULL,
    code VARCHAR(50) NOT NULL,
    name VARCHAR(120) NOT NULL,
    status VARCHAR(20) NOT NULL DEFAULT 'active',
    installed_at DATE,
    last_calibration_at DATE,
    created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    updated_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    CONSTRAINT uq_meter_facility_code UNIQUE (facility_id, code),
    CONSTRAINT ck_meter_status CHECK (status IN ('active', 'inactive', 'faulty', 'maintenance'))
);

-- =========================================================
-- Consumption and analytical data
-- =========================================================

CREATE TABLE IF NOT EXISTS monthly_consumptions (
    id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    facility_id INTEGER NOT NULL REFERENCES facilities(id) ON DELETE CASCADE,
    year INTEGER NOT NULL,
    month INTEGER NOT NULL,
    electricity_kwh DOUBLE PRECISION NOT NULL,
    water_m3 DOUBLE PRECISION NOT NULL,
    electricity_cost_usd DOUBLE PRECISION NOT NULL,
    water_cost_usd DOUBLE PRECISION NOT NULL,
    co2_avoided_ton DOUBLE PRECISION NOT NULL DEFAULT 0,
    source VARCHAR(120) NOT NULL DEFAULT 'etl',
    quality_status VARCHAR(20) NOT NULL DEFAULT 'validated',
    created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    updated_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    CONSTRAINT uq_monthly_consumption UNIQUE (facility_id, year, month),
    CONSTRAINT ck_month_range CHECK (month >= 1 AND month <= 12),
    CONSTRAINT ck_year_range CHECK (year >= 2000 AND year <= 2100),
    CONSTRAINT ck_non_negative_consumption CHECK (
        electricity_kwh >= 0 AND water_m3 >= 0 AND
        electricity_cost_usd >= 0 AND water_cost_usd >= 0 AND
        co2_avoided_ton >= 0
    ),
    CONSTRAINT ck_quality_status CHECK (quality_status IN ('raw', 'validated', 'estimated'))
);

CREATE TABLE IF NOT EXISTS area_distributions (
    id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    monthly_consumption_id INTEGER NOT NULL REFERENCES monthly_consumptions(id) ON DELETE CASCADE,
    area_name VARCHAR(80) NOT NULL,
    percentage DOUBLE PRECISION NOT NULL,
    CONSTRAINT uq_month_area UNIQUE (monthly_consumption_id, area_name),
    CONSTRAINT ck_area_distribution_percentage CHECK (percentage >= 0 AND percentage <= 100)
);

CREATE TABLE IF NOT EXISTS meter_monthly_readings (
    id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    meter_id INTEGER NOT NULL REFERENCES meters(id) ON DELETE CASCADE,
    year INTEGER NOT NULL,
    month INTEGER NOT NULL,
    consumption_value DOUBLE PRECISION NOT NULL,
    cost_usd DOUBLE PRECISION NOT NULL DEFAULT 0,
    created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    CONSTRAINT uq_meter_month UNIQUE (meter_id, year, month),
    CONSTRAINT ck_meter_month_range CHECK (month >= 1 AND month <= 12),
    CONSTRAINT ck_meter_year_range CHECK (year >= 2000 AND year <= 2100),
    CONSTRAINT ck_meter_reading_non_negative CHECK (consumption_value >= 0 AND cost_usd >= 0)
);

CREATE TABLE IF NOT EXISTS ml_models (
    id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name VARCHAR(80) NOT NULL,
    version VARCHAR(40) NOT NULL DEFAULT '1.0.0',
    algorithm VARCHAR(80) NOT NULL,
    parameters JSONB NOT NULL DEFAULT '{}'::jsonb,
    trained_at TIMESTAMPTZ,
    is_active BOOLEAN NOT NULL DEFAULT TRUE,
    created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    CONSTRAINT uq_ml_model_name_version UNIQUE (name, version)
);

CREATE TABLE IF NOT EXISTS ml_predictions (
    id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    scope VARCHAR(50) NOT NULL DEFAULT 'global',
    utility VARCHAR(30) NOT NULL,
    year INTEGER NOT NULL,
    month INTEGER NOT NULL,
    predicted_value DOUBLE PRECISION NOT NULL,
    model_name VARCHAR(80) NOT NULL DEFAULT 'RandomForestRegressor',
    validation_mae DOUBLE PRECISION NOT NULL DEFAULT 0,
    model_id INTEGER REFERENCES ml_models(id) ON DELETE SET NULL,
    facility_id INTEGER REFERENCES facilities(id) ON DELETE SET NULL,
    confidence_score DOUBLE PRECISION,
    created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    CONSTRAINT uq_prediction_scope_utility_month UNIQUE (scope, utility, year, month),
    CONSTRAINT ck_prediction_month_range CHECK (month >= 1 AND month <= 12),
    CONSTRAINT ck_prediction_year_range CHECK (year >= 2000 AND year <= 2100)
);

-- =========================================================
-- Security and users (N:M relationships included)
-- =========================================================

CREATE TABLE IF NOT EXISTS app_users (
    id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    email CITEXT NOT NULL UNIQUE,
    full_name VARCHAR(160) NOT NULL,
    is_active BOOLEAN NOT NULL DEFAULT TRUE,
    created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    updated_at TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

CREATE TABLE IF NOT EXISTS roles (
    id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name VARCHAR(50) NOT NULL UNIQUE,
    description TEXT
);

CREATE TABLE IF NOT EXISTS app_user_roles (
    user_id INTEGER NOT NULL REFERENCES app_users(id) ON DELETE CASCADE,
    role_id INTEGER NOT NULL REFERENCES roles(id) ON DELETE CASCADE,
    assigned_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    PRIMARY KEY (user_id, role_id)
);

-- =========================================================
-- Alerts and governance
-- =========================================================

CREATE TABLE IF NOT EXISTS smart_alerts (
    id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    severity VARCHAR(20) NOT NULL,
    title VARCHAR(160) NOT NULL,
    description TEXT NOT NULL,
    utility VARCHAR(30),
    year INTEGER,
    month INTEGER,
    is_resolved BOOLEAN NOT NULL DEFAULT FALSE,
    metadata JSONB NOT NULL DEFAULT '{}'::jsonb,
    facility_id INTEGER REFERENCES facilities(id) ON DELETE SET NULL,
    monthly_consumption_id INTEGER REFERENCES monthly_consumptions(id) ON DELETE SET NULL,
    assigned_user_id INTEGER REFERENCES app_users(id) ON DELETE SET NULL,
    resolved_at TIMESTAMPTZ,
    created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    CONSTRAINT ck_smart_alert_severity CHECK (severity IN ('critical', 'warning', 'info')),
    CONSTRAINT ck_smart_alert_month_range CHECK (month IS NULL OR (month >= 1 AND month <= 12)),
    CONSTRAINT ck_smart_alert_year_range CHECK (year IS NULL OR (year >= 2000 AND year <= 2100))
);

CREATE TABLE IF NOT EXISTS alert_configs (
    id INTEGER PRIMARY KEY,
    electricity_threshold_pct DOUBLE PRECISION NOT NULL DEFAULT 20.0,
    water_threshold_pct DOUBLE PRECISION NOT NULL DEFAULT 18.0,
    volatility_threshold_pct DOUBLE PRECISION NOT NULL DEFAULT 15.0,
    notify_email BOOLEAN NOT NULL DEFAULT TRUE,
    notify_in_app BOOLEAN NOT NULL DEFAULT TRUE,
    updated_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    CONSTRAINT ck_alert_thresholds CHECK (
        electricity_threshold_pct > 0 AND
        water_threshold_pct > 0 AND
        volatility_threshold_pct > 0
    )
);

CREATE TABLE IF NOT EXISTS alert_subscriptions (
    id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id INTEGER NOT NULL REFERENCES app_users(id) ON DELETE CASCADE,
    facility_id INTEGER NOT NULL REFERENCES facilities(id) ON DELETE CASCADE,
    utility VARCHAR(30),
    min_severity VARCHAR(20) NOT NULL DEFAULT 'warning',
    created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    CONSTRAINT uq_alert_subscription UNIQUE (user_id, facility_id, utility),
    CONSTRAINT ck_min_severity CHECK (min_severity IN ('critical', 'warning', 'info'))
);

