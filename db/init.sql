    metric_name VARCHAR(80) NOT NULL,
    target_value DOUBLE PRECISION NOT NULL,
    unit VARCHAR(30) NOT NULL,
    start_date DATE,
    end_date DATE,
    created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
