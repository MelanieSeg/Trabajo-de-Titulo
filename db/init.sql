CREATE OR REPLACE VIEW v_company_monthly_consumption AS
SELECT
    c.id AS company_id,
    c.name AS company_name,
    mc.year,
    mc.month,
    SUM(mc.electricity_kwh) AS total_electricity_kwh,
    SUM(mc.water_m3) AS total_water_m3,
    SUM(mc.electricity_cost_usd + mc.water_cost_usd) AS total_cost_usd,
    SUM(mc.co2_avoided_ton) AS total_co2_avoided_ton
FROM companies c
JOIN facilities f ON f.company_id = c.id
JOIN monthly_consumptions mc ON mc.facility_id = f.id
GROUP BY c.id, c.name, mc.year, mc.month;

CREATE OR REPLACE VIEW v_open_alerts_by_facility AS
SELECT
    f.id AS facility_id,
    f.name AS facility_name,
    COUNT(sa.id) FILTER (WHERE sa.is_resolved = FALSE) AS open_alerts,
    COUNT(sa.id) FILTER (WHERE sa.severity = 'critical' AND sa.is_resolved = FALSE) AS critical_open_alerts
FROM facilities f
LEFT JOIN smart_alerts sa ON sa.facility_id = f.id
GROUP BY f.id, f.name;

